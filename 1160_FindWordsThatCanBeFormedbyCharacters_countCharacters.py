import copy

class Solution(object):
    def countCharacters(self, words, chars):
        """
        :type words: List[str]
        :type chars: str
        :rtype: int
        """
        ht =dict()
        
        for c in chars:
            if c in ht.keys():
                ht[c] += 1
            else:
                ht[c] = 1
                
        rslt = 0    
        for word in words:
            cnt = 0
            temp = copy.deepcopy(ht)
            for c in word:
                if c in temp.keys():
                    cnt += 1
                    temp[c] -= 1
                    if temp[c] == 0:
                        del temp[c]
                else:
                    cnt = 0
                    break
            rslt += cnt
        return rslt
    
    
